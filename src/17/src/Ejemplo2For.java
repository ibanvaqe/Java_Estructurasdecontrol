//clase principal
public class Ejemplo2For {
//arranque del programa
    public static void main(String[] args) {
//se define la variable
        int i;
//el valor i es 10, si el valor de la variable es mayor que 0 , hacer un autorestar -1 y mostrarlo por pantalla repetidamente hasta que el valor sea 0 y se termine la instruccion.
        for(i=10; i>0;i--)
            System.out.println(i + " ");
    }
}
